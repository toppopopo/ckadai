#include<stdio.h>

/*----------------------------------------------*/
/*関数名:main					*/
/*戻り値:正常終了：０				*/
/*引　数:なし					*/
/*----------------------------------------------*/
int main(void)
{
	int i, j;


	printf("  |  1  2  3  4  5  6  7  8  9\n");
	printf("-+----------------------------\n");

	for ( i = 1 ; i <= 9 ; i++ ) {
		printf("%2d|",i);
		for ( j = 1 ; j <= 9 ; j++ ) {
			printf("%3d",i*j);
		}
		printf("\n");
	}

	return 0;
}

