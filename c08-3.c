int main(void)
{

	int d8, d10, d16;
	double f, fe;

	d8  = 0123;	/* 8進数の123*/
	d10 = 123;	/*10進数の123*/
	d16 = 0x123;	/*16進数の123*/
	f   = 1.2345;	/*小数点表現 */
	fe  = 5e3;	/*指数表現   */

	printf(" d8=%4d\nd10=%4d\nd16=%4d\n", d8, d10, d16);
	printf("  f=%7.2f\n fe=%7.2f\n", f, fe);

	return 0;
}