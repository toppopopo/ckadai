#include<stdio.h>

/*----------------------------------------------*/
/*関数名:main					*/
/*戻り値:正常終了：０				*/
/*引　数:なし					*/
/*----------------------------------------------*/
int main(void)
{
	int i, sum = 0;


	/*1から5までの合計を求める*/
	for (i = 1 ; i <=5 ; i++ )
		sum = sum + i;

	printf("sum=%d\n", sum);	/*結果出力*/

	return 0;
}