#include<stdio.h>

/*----------------------------------------------*/
/*関数名:main					*/
/*戻り値:正常終了：０				*/
/*引　数:なし					*/
/*----------------------------------------------*/
int main(void)
{
	int i, sum;


	/*複数の分を制御する場合は｛ と ｝で囲む*/
	for (sum = 0, i = 1 ; i <= 5 ; i++ ) {
		sum = sum + i;
		printf("i=%d sum=%2d\n", i, sum);
	}

	return 0;
}