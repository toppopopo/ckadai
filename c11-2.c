/*----------------------------------------------*/
int main(void)
{
	int i, sum;


	i = 1;
	sum = 0;
	while ( i <= 10 ) {
		sum += i;
		i++;
	}

	printf("1〜10までの合計は「%d」です\n", sum);

	return 0;
}