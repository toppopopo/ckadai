#include<stdio.h>

/*----------------------------------------------*/
/*関数名:main					*/
/*戻り値:正常終了：０				*/
/*引　数:なし					*/
/*----------------------------------------------*/
int main(void)
{
	int i, cnt, sum ;


	printf("正の整数を入力[1-10] :");
	scanf("%d", &cnt);

	for (i=1,sum=0; i <= cnt ; i++ ) {
		sum = sum + i;
		printf("i=%d sum=%d\n", i, sum);
	}

	return 0;
}